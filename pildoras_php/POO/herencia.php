		La herencia en programación es la similitud de características y metodos entre una instancia y otra. Es la posibilidad de crear una clase diferente, conservando las mismas propiedades y metodos de otra clase a la cual se le hace referencia.

		TERMINOLOGÍA QUE DEBEMOS CONOCER

		- SUPERCLASE: Es la clase padre de otra clase
		- SUBCLASE: Es la clase hija de otra clase

		Nota: Una clase puede ser SUPERCLASE de una clase y SUBCLASE de otra, es totalmente posible. De hecho, es muy normal que así pase.
	
