Operadores de incremento y decremento

++$a => Primero le suma una unidad a la variable $a y luego retorna el valor de $a
$a++ => Primero retorna el valor de $a y le suma una unidad
--$a => Primero le resta una unidad a la variable $a y luego retorna el valor de $a
$a-- => Primero retorna el valor de $a y le resta una unidad

*/

